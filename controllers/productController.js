//product list based on page
export const productListController = async (req, res) => {
  try {
    const perPage = 6;                                    //no. of prdcts each page
    const page = req.params.page ? req.params.page : 1;   //retrieves the current page number from request parameters and if no page parameter provided , default is 1 ( first page )
    const products = await productModel                   
      .find({})
      .select("-photo")                                //This uses the select operator to exclude the photo field from the retrieved documents. This can be useful for optimizing performance when dealing with large images or if you only need specific product information.
      .skip((page - 1) * perPage)                     //This part of the find() method uses the skip operator to determine which documents to skip at the beginning of the results based on the current page and the number of items per page. For example, on page 2 with 6 items per page, it would skip the first 6 documents.
      .limit(perPage)
      .sort({ createdAt: -1 });                         //.sort({ createdAt: -1 }): This part uses the sort operator to order the retrieved documents by their createdAt field in descending order (newest products first). You can modify the field and direction (1 for ascending) for different sorting preferences.

      res.status(200).send({
        success:true,
        products,
      })
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error in per page ctrl",
      error,
    });
  }
};


// Search product 
export const searchProductController =async(req,res)=>{
  try {
    const {keyword}=req.params;
    const results=await productModel.find({
      $or:[                                                    //the search will match products that satisfy at least one of the specified criteria.
        {name:{$regex:keyword , $options:"i"}},   
        {description:{$regex:keyword,$options:"i"}}            //The $regex operator specifies that the keyword should be treated as a regular expression.   // The $options: "i" option makes the search case-insensitive, allowing matches regardless of the capitalization of the keyword and the name field.
      ]
    }).select("-photo");
    res.json(results);
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success:false,
      message:'Error in Search Product API',
      error
    })
  }
}


